# int float list tuple dict string long boolean
# 使用type()函数的方法判断变量的类型
# int
a = 1
print(a)
print(type(a))

# float
b = 3.2
print(b)
print(type(b))

# boolean
c = True
d = False
print(a, b)
print(type(c), type(d))

# string
e = '中国'
print(e, type(e))

# list
f = [1, 2, 3, 4]
print(f, type(f))

# tuple
g = (1, 2, 3, 4)
print(g, type(g))

# dict
h = {'name': 'zds'}
print(h, type(h))
